    private void llenarTabla(){
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel)tabla.getModel();
        int n = lista.size();
        int r = model.getRowCount();
        for (int j=0; j<r; j++){
            model.removeRow(0);
        }
        for (int i=0; i<n; i++){
            Object o[] = {lista.get(i).getProducto().getNombre(), lista.get(i).getCantidad(), lista.get(i).getFechaCaducidad().toString()};
            model.addRow(o);
        }
    }

